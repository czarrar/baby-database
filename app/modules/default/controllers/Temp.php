$notFound = array();

$rowMin = trim($this->_getParam("s"));
$rowMax = trim($this->_getParam("e"));

if(empty($rowMin) or empty($rowMax)) {
	echo "Set 's' and 'e'!";
	exit();
}

$rowNum = 1;
$handle = fopen("/Users/czarrar/Downloads/DbInformation/" . "z_contacthistory.csv", "r");
while (($data = fgetcsv($handle)) !== FALSE) {
	// Skip header
	$rowNum++;
	if ($rowNum == 2)
		continue;
	elseif ($rowNum < $rowMin)
		continue;
	elseif ($rowNum > $rowMax)
		break;

	// Study
	$study = trim($data[4]);
	if (!empty($study)) {
		$key = array_search($study, $origStudies);

		if ($key === false) {
			$keyExist = array_key_exists($study, $notFound);
			
			if ($keyExist)
				$notFound{$study} = $notFound{$study} + 1;
			else
				$notFound{$study} = 1;
		}
	}
}

// Arrange names in order
ksort($notFound);

#var_export($notFound);

// Write to csv
$fp = fopen("/Users/czarrar/Downloads/DbInformation/z_additional_studies1.csv", 'w');
$line = array();

foreach ($notFound as $name => $num) {
	$line[0] = $name;
	$line[1] = $num;
	fputcsv($fp, $line);
}

fclose($fp);

